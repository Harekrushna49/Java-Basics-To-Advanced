public class Arrays {
        public static void main(String[] args) {

            //1. 1-dimensional Array
            int a [] = new int[5];
            a[0] = 234;
            a[1] = 865;
            a[2] = 310;
            a[3] = 468;
            a[4] = 975;
            System.out.println(a[4]);

            // another type of initialisation.
            int l [] = {1478, 865, 7893, 4874, 8224};
            System.out.println(l[3]);
            System.out.println(l.length);
        }
}
